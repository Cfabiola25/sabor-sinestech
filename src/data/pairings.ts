const normalizeDishId = (value: string) =>
  value
    .normalize("NFD")
    .replace(/[\u0300-\u036f]/g, "")
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");

const getCanonicalDishId = (dishId: string): string => {
  const normalized = normalizeDishId(dishId);

  const directMatch = Object.keys(dishIdAliases).find(
    (id) => normalizeDishId(id) === normalized
  );

  if (directMatch) return directMatch;

  const aliasMatch = Object.entries(dishIdAliases).find(([, aliases]) =>
    aliases.some((alias) => normalizeDishId(alias) === normalized)
  );

  return aliasMatch?.[0] ?? dishId;
};

const getDishImageUrl = (dishId: string): string | undefined => {
  const canonicalDishId = getCanonicalDishId(dishId);
  const dish = getDishById(canonicalDishId) ?? getDishById(dishId);
  const imageFileName = dish?.image?.trim();

  if (!imageFileName) return undefined;

  return (
    dishImagesFromDishesFolder[`../assets/images/dishes/${imageFileName}`] ??
    dishImagesFromImagesFolder[`../assets/images/${imageFileName}`]
  );
};

const withCompatibilityFields = (pairing: Pairing): Pairing => {
  const dishImageUrl = getDishImageUrl(pairing.dishId);

  return {
    ...pairing,
    justification: pairing.justification ?? pairing.pairingReason,
    description: pairing.description ?? pairing.pairingReason,
    culturalContext: pairing.culturalContext ?? pairing.technicalReview,
    imageUrl: dishImageUrl ?? pairing.imageUrl ?? pairing.experience.imageUrl,
    experience: {
      ...pairing.experience,
      imageUrl: dishImageUrl ?? pairing.experience.imageUrl ?? pairing.imageUrl,
    },
  };
};

const expandedAliasPairings = basePairings.flatMap((pairing) => {
  const aliases = dishIdAliases[pairing.dishId] ?? [];

  return aliases.map((alias) =>
    withCompatibilityFields({
      ...pairing,
      dishId: alias,
    })
  );
});

export const pairings: Pairing[] = [
  ...basePairings.map(withCompatibilityFields),
  ...expandedAliasPairings,
];

export const getPairingsByDishId = (dishId: string): Pairing[] => {
  const canonicalDishId = getCanonicalDishId(dishId);

  return basePairings
    .filter((pairing) => pairing.dishId === canonicalDishId)
    .map(withCompatibilityFields);
};

export const getPairingByDishId = (dishId: string): Pairing | null => {
  const canonicalDishId = getCanonicalDishId(dishId);

  const foundPairing = basePairings.find(
    (pairing) => pairing.dishId === canonicalDishId
  );

  return foundPairing ? withCompatibilityFields(foundPairing) : null;
};

export const getPairingReview = (dishId: string): string => {
  const dish = getDishById(dishId);
  const dishPairings = getPairingsByDishId(dishId);

  if (!dish || dishPairings.length === 0) {
    return "No hay revisión disponible para este platillo.";
  }

  const bestPairing = [...dishPairings].sort((a, b) => b.score - a.score)[0];
  return `${dish.name}: mejor maridaje sugerido: ${bestPairing.pairingName}. ${bestPairing.technicalReview}`;
};